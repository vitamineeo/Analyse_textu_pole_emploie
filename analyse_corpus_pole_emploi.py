"""---
## 📐 Bloc 6 — Richesse lexicale (Type-Token Ratio)
"""

# ── Le TTR mesure la diversité du vocabulaire dans chaque offre ──────────────
# TTR = nb de mots uniques / nb total de mots
# Un TTR élevé = texte varié ; un TTR faible = texte répétitif

def calcul_ttr(texte):
    """Calcule le Type-Token Ratio d'un texte."""
    mots = re.findall(r'\b\w+\b', str(texte).lower())
    if len(mots) == 0:
        return 0
    return len(set(mots)) / len(mots)

df['ttr'] = df['textual description'].apply(calcul_ttr)

# ── Histogramme du TTR global ────────────────────────────────────────────────
fig, ax = plt.subplots(figsize=(10, 4))
ax.hist(df['ttr'], bins=40, color='mediumseagreen', edgecolor='white')
ax.axvline(df['ttr'].median(), color='red', linestyle='--', label=f"Médiane : {df['ttr'].median():.2f}")
ax.set_title("Distribution du Type-Token Ratio (richesse lexicale)", fontsize=13)
ax.set_xlabel("TTR (0 = très répétitif, 1 = très varié)")
ax.set_ylabel("Fréquence")
ax.legend()
plt.tight_layout()
plt.show()

# ── TTR moyen par catégorie ───────────────────────────────────────────────────
