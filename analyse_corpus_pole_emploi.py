"""---
## 🔁 Bloc 7 — Détection des quasi-doublons
"""

# ── Calcul de similarité cosine sur un échantillon ───────────────────────────
# On travaille sur un échantillon pour éviter les calculs trop lourds
TAILLE_ECHANTILLON = min(500, len(df))   # augmente si ton corpus n'est pas trop grand
echantillon = df.sample(TAILLE_ECHANTILLON, random_state=42)

# Vectorisation TF-IDF de l'échantillon
vec_doublons = TfidfVectorizer(
    stop_words=list(STOPWORDS_FR),
    max_features=3000
)
mat_ech = vec_doublons.fit_transform(echantillon['textual description'].fillna(''))

# Calcul de la matrice de similarité cosine
sim_matrix = cosine_similarity(mat_ech)
np.fill_diagonal(sim_matrix, 0)   # on ignore la similarité d'un texte avec lui-même

print(f"Matrice de similarité calculée : {sim_matrix.shape}")

# ── Heatmap de similarité (sous-échantillon de 50 offres pour la lisibilité) ─
N_HEATMAP = min(1000, TAILLE_ECHANTILLON)

fig, ax = plt.subplots(figsize=(10, 8))
sns.heatmap(
    sim_matrix[:N_HEATMAP, :N_HEATMAP],
    cmap='Reds',
    vmin=0, vmax=1,
    ax=ax,
    xticklabels=False,
    yticklabels=False
)
ax.set_title(f"Heatmap de similarité cosine (sous-échantillon {N_HEATMAP} offres)", fontsize=13)
plt.tight_layout()
plt.show()

# ── Comptage des quasi-doublons (similarité > seuil) ─────────────────────────
SEUIL_DOUBLON = 0.85

# On prend le triangle supérieur pour éviter de compter les paires deux fois
tri_sup = np.triu(sim_matrix, k=1)
nb_quasi_doublons = (tri_sup > SEUIL_DOUBLON).sum()

print(f"Paires d'offres avec similarité > {SEUIL_DOUBLON} : {nb_quasi_doublons}")
print(f"(sur l'échantillon de {TAILLE_ECHANTILLON} offres)")
print("→ Ces paires sont des quasi-doublons potentiels")

# ── Distribution des scores de similarité maximale par offre ─────────────────
sim_max_par_offre = sim_matrix.max(axis=1)

fig, ax = plt.subplots(figsize=(10, 4))
ax.hist(sim_max_par_offre, bins=40, color='tomato', edgecolor='white')
ax.axvline(SEUIL_DOUBLON, color='black', linestyle='--', label=f'Seuil doublon ({SEUIL_DOUBLON})')
ax.set_title("Similarité maximale de chaque offre avec les autres", fontsize=13)
ax.set_xlabel("Score de similarité cosine")
ax.set_ylabel("Nombre d'offres")
ax.legend()
plt.tight_layout()
plt.show()

