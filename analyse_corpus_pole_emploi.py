# ── Génération de la palette : une nuance différente par code_rome ────────────
# On fait varier la luminosité au sein de chaque groupe de lettres
import colorsys

def hex_to_rgb(h):
    """Convertit un code hex en tuple RGB (0-1)."""
    h = h.lstrip('#')
    return tuple(int(h[i:i+2], 16) / 255 for i in (0, 2, 4))

def rgb_to_hex(r, g, b):
    """Convertit un tuple RGB (0-1) en code hex."""
    return '#{:02x}{:02x}{:02x}'.format(int(r*255), int(g*255), int(b*255))

def varier_luminosite(hex_base, n, index):
    """
    Génère n nuances d'une couleur de base en faisant varier la luminosité.
    index = position du code dans son groupe (0, 1, 2...)
    """
    r, g, b = hex_to_rgb(hex_base)
    h, l, s = colorsys.rgb_to_hls(r, g, b)
    # On fait varier la luminosité entre 30% et 70%
    if n == 1:
        l_new = 0.50
    else:
        l_new = 0.30 + (index / (n - 1)) * 0.40
    r2, g2, b2 = colorsys.hls_to_rgb(h, l_new, s)
    return rgb_to_hex(r2, g2, b2)

